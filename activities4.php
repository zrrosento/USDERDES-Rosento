					<h2>Activity 4: Need Finding</h2>
					<p><style> p {text-indent:50px;t}</style>our idea came from somewhere near home in APC; the gaming community, specifically the Game Lab on the 5th floor. It’s a somewhat new development and has had its fair share of problems overtime. As a simple introduction, our project is an online reservation system that allows the user(students and professors alike) to reserve a time slot to use a specific asset in the Game Lab. As an important note, according to a friend of ours who is a Game Development Professor, the lab is occasionally used for classes. Professors would have less time constraints when making a reservation for that purpose, and their classes would be given the upmost priority when using the lab. Students on the other hand would have a maximum window they may reserve, provided the equipment to be used is available, and that the period will not be over its capacity. Our aim with the system is to turn the lab’s noticeable issues into opportunities for improvement via our proposed system.  The primary target areas would be the following:
						<p>•	The crowded numbers of students in the lab</p>
						<p>•	Maintenance of the Lab’s assets(board/video games, consoles, etc.)
						One incident was a major influence in our decision for this project. This was when a student reportedly took away one of the games without permission and never returned it. We want our application to create a link between the users and the equipment for the sake of keeping better eyes on the lab’s assets, and to prevent incidents like these from recurring.
						You may be asking yourself, “why an online reservation system when a sign-up book already exists in the lab?” We believe the simple and honest answer would be two words; efficiency and accessibility. Why go all the way to the lab to see who is using what on a book when you can just access it online? We see the proposed system as a viable solution for the following reasons:</p>
						<p>•	Reservations may be made in advance via a website rather than manually made on text.</p>
						<p>•	It helps prioritize professors when they need the Lab for lectures and the such.</p>
						<p>•	It allows the student heads/professors to monitor these reservations and equipment liabilities via the online viewing system.</p>
						<p>•	A website or online application may open up opportunities for the use of color and iconography to symbolize the lab and its assets, making it more user-friendly.</p>
